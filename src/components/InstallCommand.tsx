'use client';

import { useState } from 'react';

export default function InstallCommand({ slug }: { slug: string }) {
  const [copied, setCopied] = useState(false);
  const command = `install the ${slug} skill from https://github.com/BankrBot/openclaw-skills/tree/main/${slug}`;

  const handleCopy = async () => {
    await navigator.clipboard.writeText(command);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="mt-4 flex items-center justify-between gap-4 rounded-xl bg-[#1a1a1a] px-5 py-3.5">
      <p className="font-mono text-sm text-neutral-300 truncate">
        <span className="text-neutral-500">$ </span>
        {command}
      </p>
      <button
        onClick={handleCopy}
        className="shrink-0 text-neutral-500 hover:text-neutral-300 transition-colors cursor-pointer"
        title="Copy to clipboard"
      >
        {copied ? (
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
          </svg>
        ) : (
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M8 16H6a2 2 0 01-2-2V6a2 2 0 012-2h8a2 2 0 012 2v2m-6 12h8a2 2 0 002-2v-8a2 2 0 00-2-2h-8a2 2 0 00-2 2v8a2 2 0 002 2z"
            />
          </svg>
        )}
      </button>
    </div>
  );
}
